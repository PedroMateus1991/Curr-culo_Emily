tupla = (1, 2, 3, 4, 5)
menor = min(tupla)
maior = max(tupla)

print(f"O menor valor da tupla é {menor}")
print("O maior valor da tupla é {}".format(maior))

print("\n")

print("O menor valor da tupla é: ", menor)
print("O maior valor da tupla é: ", maior)


